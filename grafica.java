    private void jButtonListarHistoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarHistoricoActionPerformed
        int idEquipamentoDomestico = Integer.parseInt(jButtonRemoverProdEnergia.getText());
        for(Integer i : rede.getCasasST().keys()){
            Casa c = rede.getCasasST().get(i);
            for(Integer j : c.getEquipamentosDomesticosST().keys()){
                EquipamentoDomestico ed = c.getEquipamentosDomesticosST().get(j);
                if(ed.getIdEquipamentoDomestico() == idEquipamentoDomestico){
                    for(Integer k : ed.getHistoricoUtilizacaoST().keys()){
                        HistoricoUtilizacao hu = ed.getHistoricoUtilizacaoST().get(k);
                        this.jTextAreaHistorico.setText(hu.toString());
                    }
                }
            }
        }
    }//GEN-LAST:event_jButtonListarHistoricoActionPerformed

