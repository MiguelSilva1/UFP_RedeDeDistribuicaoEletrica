    private void jButtonRemoverEqDomesticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverEqDomesticoActionPerformed
        int idEquipamentoDomestico = Integer.parseInt(jTextFieldEqDomestico.getText());
        //Remove da RedBlack na classe Casa
        for (Integer i : rede.getCasasST().keys()) {
            Casa c = rede.getCasasST().get(i);
            c.removerEquipamentoDomestico(idEquipamentoDomestico);
        }
        //Remove da RedBlack auxiliar na classe Rede
        for (Integer j : rede.getEquipamentosDomesticosST().keys()) {
            if (rede.getEquipamentosDomesticosST().get(j).getIdEquipamentoDomestico() == (idEquipamentoDomestico)) {
                rede.getEquipamentosDomesticosST().delete(j);
            }
        }
        jTextAreaRemover.setText("Equipamento doméstico removido com sucesso! \n");

        //Limpa as jTextField após inserção.
        this.jTextFieldEqDomestico.setText("");
    }//GEN-LAST:event_jButtonRemoverEqDomesticoActionPerformed

