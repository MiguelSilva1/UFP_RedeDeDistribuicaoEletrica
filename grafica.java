    private void jButtonInserirCasaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirCasaActionPerformed
        int idZona = Integer.parseInt(jTextFieldIDZona.getText());
        Float potenciaContador = Float.parseFloat(jTextFieldPotenciaContador.getText());
        float somaPotenciasEquipamentosDomesticos = 0.0f;
        float somaEnergiaEquipamentosProdutores = 0.0f;

        Casa c = new Casa(potenciaContador, somaPotenciasEquipamentosDomesticos, somaEnergiaEquipamentosProdutores, idZona);
        rede.getCasasST().put(c.getIdCasa(), c);
        this.jComboBoxCasa.addItem(Integer.toString(c.getIdCasa()));

        //Limpa as jTextField após inserção.
        this.jTextFieldIDZona.setText("");
        this.jTextFieldPotenciaContador.setText("");
    }//GEN-LAST:event_jButtonInserirCasaActionPerformed

